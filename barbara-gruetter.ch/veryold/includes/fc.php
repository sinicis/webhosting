<p>Vor allem die falsche Ern&auml;hrung mit industriellem, minderwertigem, gleichzeitig aber als Premium-Nahrung bevorbenem Tierfutter 
ist die Grundlage f&uuml;r zahlreiche Erkrankungen. Die dramatisch angestiegenen zahlen an Krebserkrankungen von Hund und katze zeigen 
dies analog zur immer vielf&auml;ltiger werdenden Auswahl an minderwertigem Teirfutter in Superm&auml;rkten, Online-Shops und sogenannten 
Fachgesch&auml;ften. Auch unsere Tier&auml;rtze tragen durch falsche Informationen, die die Ern&auml;hrung unserer haustiere betreffen, 
dazu bei ihre Patienten in chronische Erkrankungen hineinzutreiben. Der Satz "Da ist ja alles drin, was Hund und Katze brauchen" ist 
der verh&auml;ngnisvolle Irrglaube, den uns die Futtermittelindustrie weismachen will.<br /><br />
(Quelle Dr. med. vet. Jutta Ziegler)<br /><br />
