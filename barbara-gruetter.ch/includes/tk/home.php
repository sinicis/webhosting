			<h2>Tierkommunikation</h2>
			Tierkommunikation ist die telepathische Kommunikation mit Tieren. Jedem gesprochenen Wort geht ein Gedanke, 
			ein Bild oder ein Gef&uuml;hl voran. Telepathische Kommunikation ist nichts anderes als eine Sensibilisierung 
			der Sinne, ein bewusst gemachtes Empfangen und Senden von Gefühlen, Gedanken, Bildern, Worten, K&ouml;rperbefindlichkeiten 
			oder einfach Wissen. Tiere kommunizieren mit ihrer K&ouml;rpersprache, mit Lauten, aber auch st&auml;ndig telepathisch 
			mit Artgenossen und auch mit anderen Spezies wie dem Menschen.<br /><br />
			Der Tierkommunikator kann ein Dolmetscher zwischen dem Tier und seinem Besitzer sein. Dabei ist es besonders wichtig, 
			dass Informationen, welche vom Tier stammen, m&ouml;glichst unverf&auml;lscht an den Besitzer &uuml;bermittelt werden.
