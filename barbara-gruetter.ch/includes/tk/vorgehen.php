<h2>Vorgehen</h2>
<p>
Wie gehen Sie vor, wenn Sie mit Ihrem Tier kommunizieren m&ouml;chten?</br></br>
Sie nehmen telefonisch, per E-Mail oder via Website Kontakt mit mir auf und vermitteln mir folgende Angaben zu Ihrem 
Tier:
</p>

<ul>
	<li>Name, Alter, Rasse</li>
	<li>wenn m&ouml;glich ein Foto</li>
	<li>eine Auflistung Ihrer Fragen</li>
	<li>Ihre Postanschrift und Telefonnummer</li>
</ul>

<p>
Nachdem ich mit Ihrem Tier Kontakt aufgenommen habe, werde ich Ihnen das Gespr&auml;chsprotokoll m&uuml;ndlich am 
Telefon erl&auml;utern.</br></br>
Notf&auml;lle wie:
</p>

<ul>
	<li>akute Krankheitssymptome mit grossen Schmerzen</li>
	<li>Entscheidung &uuml;ber das Einschl&auml;fern</li>
	<li>dringender Handlungsbedarf bei in Not geratener Tiere</li>
</ul>

<p>
werden selbstverst&auml;ndlich bevorzugt behandelt.
</p>

