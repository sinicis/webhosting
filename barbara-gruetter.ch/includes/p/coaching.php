<h2>Pferdegest&uuml;tztes Coaching</h2>
<p>
Pferdegest&uuml;tztes Coaching ist ein Prozess im Sinne einer pers&ouml;nlichen Weiterentwicklung und Selbsterkenntnis mit dem Pferd als Partner. Es richtet sich an alle, die bereit sind, sich auf eine aussergew&ouml;hnliche und intensive Erfahrung einzulassen. Sei es im Business Coaching, Team Training oder im pers&ouml;nlichen Leben.</br></br>
Dem Pferd kommt dabei die Vermittlerrolle zu, wobei nie geritten wird. Das Pferd wird also als Medium f&uuml;r einen zielgerichteten Prozess verwendet, steht stellvertretend f&uuml;r eine Situation, einen Prozess im Arbeits- und Alltagsleben der Teilnehmer/innen. Im Unterschied zur Hippotherapie verfolgt es keine therapeutischen Ziele.
</p>

