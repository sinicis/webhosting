<h2>Pferde als Spiegel der Seele</h2>

<table align='center'>
<tr><td>
Der Mensch sieht im Pferd nur zum Teil tats&auml;chlich das Pferd. Was er ausserdem findet, ist ein Abbild seiner eigenen, inneren Landschaft, eine Projektion seiner Seele. Der eine 
sieht im Pferd die Kraft, der Zweite die Sch&ouml;nheit, ein Dritter die Sanftheit... alles sind Aspekte des Pferdes und doch zugleich Projektionen der Kraft, der Sch&ouml;nheit, der 
Sanftheit des Betrachtenden. So begegnet der Mensch in unserem Spiegel seinem "Schatten", gemeint sind jene Anteile seiner Pers&ouml;nlichkeit, die er versucht hat, ins Unbewusste zu 
verdr&auml;ngen, und die sich nun in seiner Wahrnehmung des Pferdes oder auch in der konkreten Resonanz des Tieres offenbaren.</br></br>
(Quelle Gerhard Konir dipl. Lebens- und Sozialberater, Mediator, Lehrtrainer und Lehrcoach)
